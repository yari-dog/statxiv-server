function sendPayload(payload) {
    device.write([0, 0x16]);
    for (let i = 0; i < 16; i++) {
        let current_line = [0x0];
        for (let j = 0; j < 32; j++) {
            current_line.push(payload[(Number(i) * 32) + Number(j)]);
        }
        device.write(current_line);
    }
    device.write([0, 0x32]);
}

function getKeyboard() {
    devices = HID.devices();
    keyboard = devices.find((device) => {
        return device.product === KEYBOARD_NAME && device.usagePage === KEYBOARD_USAGE_PAGE && device.usage === KEYBOARD_USAGE_ID;
    });

    if (keyboard) {
        device = new HID.HID(keyboard.path);
        device.on("data", (data) => {
            console.log(data);
        });
        device.on("error", (error) => {
            console.log(error);
        });
        console.log('Found keyboard');
    } else {
        console.log('Keyboard not found');
    }
}

function updateKeyboard() {
    if (device) {
        try {
            sendPayload(payload);
        } catch (error) {
            console.log('Error writing to keyboard');
            device = null;
        }
    }
    else {
        findBoard();
    }
}
function findBoard(){
    if (!device) {
        getKeyboard();
    }
}

